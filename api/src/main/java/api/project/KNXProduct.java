package api.project;

public class KNXProduct {

	private String hardwareId;

	private String hardwareName;

	private String hardwareSerialNumber;

	private String hardwareVersionNumber;

	private String productId;

	private String productText;

	private String productOrderNumber;

	private String applicationProgramRef;

	public String getHardwareId() {
		return hardwareId;
	}

	public void setHardwareId(final String hardwareId) {
		this.hardwareId = hardwareId;
	}

	public String getHardwareName() {
		return hardwareName;
	}

	public void setHardwareName(final String hardwareName) {
		this.hardwareName = hardwareName;
	}

	public String getHardwareSerialNumber() {
		return hardwareSerialNumber;
	}

	public void setHardwareSerialNumber(final String hardwareSerialNumber) {
		this.hardwareSerialNumber = hardwareSerialNumber;
	}

	public String getHardwareVersionNumber() {
		return hardwareVersionNumber;
	}

	public void setHardwareVersionNumber(final String hardwareVersionNumber) {
		this.hardwareVersionNumber = hardwareVersionNumber;
	}

	public String getProductId() {
		return productId;
	}

	public void setProductId(final String productId) {
		this.productId = productId;
	}

	public String getProductText() {
		return productText;
	}

	public void setProductText(final String productText) {
		this.productText = productText;
	}

	public String getProductOrderNumber() {
		return productOrderNumber;
	}

	public void setProductOrderNumber(final String productOrderNumber) {
		this.productOrderNumber = productOrderNumber;
	}

	public String getApplicationProgramRef() {
		return applicationProgramRef;
	}

	public void setApplicationProgramRef(final String applicationProgramRef) {
		this.applicationProgramRef = applicationProgramRef;
	}

}
